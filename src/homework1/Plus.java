package homework1;

public class Plus {
    /**
     * The calculateSum() method returns the addition of 2 given numbers.
     */
    public static int calculateSum(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }

    /**
     * The calculateSumDouble() method returns the double sum of 2 given numbers.
     */
    public static int calculateSumDouble(int num1, int num2) {
        int sumDouble = (num1 + num2) * 2;
        return sumDouble;
    }
}
